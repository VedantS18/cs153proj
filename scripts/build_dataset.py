ALL_CONCEPTS = {**FACTUAL, **BIAS, **STYLISTIC}
CONCEPT_CATEGORY = (
    {k: "factual" for k in FACTUAL}
    | {k: "bias" for k in BIAS}
    | {k: "stylistic" for k in STYLISTIC}
)


def build_pairs(concept_name, data, n=200):
    pos = data["positives"]
    neg = data["negatives"]
    # oversample with replacement if needed
    pairs = []
    for text in random.choices(pos, k=n):
        pairs.append({"text": text, "label": 1, "concept": concept_name,
                      "category": CONCEPT_CATEGORY[concept_name]})
    for text in random.choices(neg, k=n):
        pairs.append({"text": text, "label": 0, "concept": concept_name,
                      "category": CONCEPT_CATEGORY[concept_name]})
    random.shuffle(pairs)
    return pairs


def main():
    out_dir = "data/concepts"
    os.makedirs(out_dir, exist_ok=True)

    for concept_name, data in ALL_CONCEPTS.items():
        pairs = build_pairs(concept_name, data, n=200)
        out_path = os.path.join(out_dir, f"{concept_name}.jsonl")
        with open(out_path, "w") as f:
            for item in pairs:
                f.write(json.dumps(item) + "\n")
        print(f"  {concept_name}: {len(pairs)} examples → {out_path}")


if __name__ == "__main__":
    main()
